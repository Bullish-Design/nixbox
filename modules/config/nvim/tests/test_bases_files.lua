T["bases regenerate writes all default files"] = helpers.async_test(function()
  local repo = helpers.create_tmpdir()
  local restore = helpers.patch_project_root(repo)
  helpers.reset_modules()

  require("loci").setup({
    integrations = { bases = true },
    refresh = { on_setup = false },
  })

  local init_r = require("loci.service.repository").init({ now = "2026-05-23T12:00:00-04:00" })
  helpers.expect_ok(init_r)

  local bases = require("loci.service.bases")
  local r = bases.regenerate({ now = "2026-05-23T12:00:00-04:00" })
  local value = helpers.expect_ok(r)

  expect.equality(value.enabled, true)
  expect.equality(#value.files, 6)

  -- Verify all files exist
  for _, file in ipairs(value.files) do
    expect.equality(vim.fn.filereadable(file.path), 1)
  end

  restore()
  helpers.remove_tmpdir(repo)
end)

