local helpers = require("tests.helpers")
local MiniTest = require("mini.test")
local expect = MiniTest.expect

local T = MiniTest.new_set()

T["bases default_specs returns six filenames"] = helpers.async_test(function()
  local bases = require("loci.service.bases")
  local specs = bases.default_specs()
  expect.equality(#specs, 6)

  local filenames = {}
  for _, spec in ipairs(specs) do
    table.insert(filenames, spec.filename)
  end
  table.sort(filenames)

  expect.equality(filenames[1], "loci-daily.base")
  expect.equality(filenames[2], "loci-issues.base")
  expect.equality(filenames[3], "loci-notes.base")
  expect.equality(filenames[4], "loci-open-work.base")
  expect.equality(filenames[5], "loci-projects.base")
  expect.equality(filenames[6], "loci-tasks.base")
end)

T["bases serialize includes generated-file header"] = helpers.async_test(function()
  local bases = require("loci.service.bases")
  local spec = {
    filename = "test.base",
    filters = { ["and"] = { "note.type == \"task\"" } },
    views = {
      {
        type = "table",
        name = "Test View",
        filters = { ["and"] = { "note.type == \"task\"" } },
        order = { "title" },
        sort = { { column = "title", direction = "ASC" } },
      },
    },
  }

  local r = bases.serialize(spec)
  helpers.expect_ok(r)
  local content = r.value

  expect.equality(content:find("Generated by LOCI") ~= nil, true)
  expect.equality(content:find("Markdown files under") ~= nil, true)
end)

